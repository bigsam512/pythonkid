class Giraffes():
    def left_foot_forward(a):
        print('left foot forward')

    def left_foot_back(self):
        print('left foot back')

    def right_foot_forward(self):
        print('right foot forward')

    def right_foot_back(self):
        print('right foot back')

    def dance(ab):
        ab.left_foot_forward()
        ab.left_foot_back()
        ab.right_foot_forward()
        ab.right_foot_back()
        ab.left_foot_back()
        ab.right_foot_back()
        ab.right_foot_forward()
        ab.left_foot_forward()

reginald = Giraffes()
reginald.dance()
